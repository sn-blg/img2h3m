use crate::h3m::parsers::{self, H3mObjectTemplate};

pub struct ObstacleTemplate {
    h3m_template: H3mObjectTemplate,
    index: usize,
}

impl ObstacleTemplate {
    fn new(h3m_template: H3mObjectTemplate) -> ObstacleTemplate {
        ObstacleTemplate {
            h3m_template,
            index: 0,
        }
    }

    pub fn h3m_template(&self) -> &H3mObjectTemplate {
        &self.h3m_template
    }

    pub fn index(&self) -> usize {
        self.index
    }

    pub fn set_index(&mut self, index: usize) {
        self.index = index;
    }
}

pub struct ObstacleTemplateList(Vec<ObstacleTemplate>);

impl ObstacleTemplateList {
    pub fn new() -> ObstacleTemplateList {
        ObstacleTemplateList(
            h3m_obstacle_templates()
                .into_iter()
                .map(ObstacleTemplate::new)
                .collect(),
        )
    }

    pub fn template(&mut self, index: usize) -> &mut ObstacleTemplate {
        &mut self.0[index]
    }

    pub fn len(&self) -> usize {
        self.0.len()
    }
}

